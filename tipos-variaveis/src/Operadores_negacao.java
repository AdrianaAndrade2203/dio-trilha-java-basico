public class Operadores_negacao {

    public static void main(String[] args) {

        // operador de negação (-)
        
        int numero = 5;

        numero = - numero;

        System.out.println(- numero);

        System.out.println(numero);

        // para tornar o valor positivo novamente

        numero = numero * -1;

        System.out.println(numero);

    }
    
}
