public class Operadores_incremento {

    public static void main(String[] args) {
        
        //Utilizando operadores para incremento

        int numero = 5;

        numero = numero + 2;

        numero ++;

        System.out.println(numero);

        boolean variavel = true;

        //Operador de negação (!)

        System.out.println(!variavel);

        System.out.println(variavel);
    }
            
    
}
